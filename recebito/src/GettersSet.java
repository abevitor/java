public class GettersSet {
    private String name;
    private int age;
    private String gen;
    private double tamanho;
    private double tamanhoPai;
    private double tamanhoMae;

    // Métodos setters
    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setGen(String gen) {
        this.gen = gen;
    }

    public void setTamanho(double tamanho) {
        this.tamanho = tamanho;
    }

    public void setTamanhoPai(double tamanhoPai) {
        this.tamanhoPai = tamanhoPai;
    }

    public void setTamanhoMae(double tamanhoMae) {
        this.tamanhoMae = tamanhoMae;
    }

    // Métodos getters
    public String getName() {
        return this.name;
    }

    public int getAge() {
        return this.age;
    }

    public String getGen() {
        return this.gen;
    }

    public double getTamanho() {
        return this.tamanho;
    }

    public double getTamanhoPai() {
        return this.tamanhoPai;
    }

    public double getTamanhoMae() {
        return this.tamanhoMae;
    }


}
